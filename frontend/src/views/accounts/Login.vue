      console.log(googleUser);
      this.googleUser = googleUser;
      this.tokenVerify()
    },
    onFailure(error) {
      // eslint-disable-next-line
      console.log(error);
    },

    signout() {
      const authInst = window.gapi.auth2.getAuthInstance();
      authInst.signOut().then(() => {
